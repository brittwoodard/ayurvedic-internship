// Light Mobile Menu
// A $( document ).ready() block.
$(document).ready(function () {
  $(document).ready(function () {
    $(".mobile-button").click(function () {
      $(".mobile-button").toggleClass("active");
      $(".mobile-menu").toggleClass("active");
    });
  });
});

